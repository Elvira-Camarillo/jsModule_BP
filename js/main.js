const NUMBER_TWO = parseInt(prompt('Escribe un segundo número diferente'))

NUMBER_ONE === NUMBER_TWO ? 
    console.log(`El ${NUMBER_ONE} es el mayor`)
    :
    console.log(`El ${NUMBER_TWO} es el mayor`)